CREATE TABLE CLIENT(
CLIENTCODE INTEGER GENERATED BY DEFAULT AS IDENTITY,

CLIENTNAME VARCHAR(16) NOT NULL,
CLIENTSURNAME VARCHAR(16) NOT NULL,
CLIENTPATRONYMIC VARCHAR(16) NOT NULL,

PASPORTDATA VARCHAR(16) NOT NULL,
COMMENTABOUTCLIENTS VARCHAR(40) NOT NULL,

PRIMARY KEY(CLIENTCODE)
);

CREATE TABLE ROOM(
ROOMCODE INTEGER GENERATED BY DEFAULT AS IDENTITY,

ROOMNUMBER INTEGER NOT NULL,
NUMBEROFSEATS INTEGER NOT NULL,
ROOMCOMFORT VARCHAR(28) NOT NULL,
PRICEPERDAY FLOAT NOT NULL,

PRIMARY KEY(ROOMCODE)
);

CREATE TABLE SETTLEMENT(
SETTLEMENTCODE INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

CLIENTCODE INTEGER NOT NULL,
ROOMCODE INTEGER NOT NULL,

DATEOFSETTLEMENT DATE NOT NULL,
DATEOFEVICTION DATE NOT NULL,

CHEKINNOTE VARCHAR(40) NOT NULL,

FOREIGN KEY (CLIENTCODE) REFERENCES CLIENT(CLIENTCODE),
FOREIGN KEY (ROOMCODE) REFERENCES ROOM(ROOMCODE)
);